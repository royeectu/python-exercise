# Write a program to create a set

# An empty set:
empty_set1 = set()
print(type(empty_set1))
print(empty_set1)

print("\n")

# Another set (not empty):
my_set = set([1, 2, 3, 4, 4, 3, 2, 1])
print(type(my_set))
print(my_set)

print("\n")

# And another set (non empty):
another_set = {31, 5, 'May'}
print(type(another_set))
print(another_set)
